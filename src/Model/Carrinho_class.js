/*Essa classe representa um carrinho de compras, e o objeto carrinho pode
ser usado pelas demais classes e passado como parametro.
*/

class Carrinho {

    constructor(id, usuario, produtos, valorTotal, estado) {
        this.id = id;
        this.usuario = usuario;
        this.produtos = produtos;
        this.valorTotal = valorTotal;
        this.estado = estado;
    }

}